// Helper function to get drugs by category
export const getDrugsByCategory = () => {
  const categories = {}
  polishDrugs.forEach(drug => {
    if (!categories[drug.kategoria]) {
      categories[drug.kategoria] = []
    }
    categories[drug.kategoria].push(drug)
  })
  return categories
}

// Helper function to search drugs
export const searchDrugs = (query) => {
  const lowercaseQuery = query.toLowerCase()
  return polishDrugs.filter(drug =>
    drug.nazwa_miedzynarodowa.toLowerCase().includes(lowercaseQuery) ||
    drug.nazwa_polska.toLowerCase().includes(lowercaseQuery) ||
    drug.nazwa_handlowa.toLowerCase().includes(lowercaseQuery) ||
    drug.kategoria.toLowerCase().includes(lowercaseQuery)
  )
}